function replyGuide(outputFormat?: OutputFormat) {
  if (outputFormat === "微信") {
    return [
      "需要生成 reply（微信格式）。要求：",
      "- 80~180 字左右，像真实聊天；",
      "- 先确认理解，再给行动/时间点，再补 1-3 个确认问题（如需要）；",
      "- 不要长段落，最多 3-5 句；",
      "- 不要使用 Markdown/序号/标题。",
    ].join("\n");
  }
  if (outputFormat === "邮件") {
    return [
      "需要生成 reply（邮件格式）。要求：",
      "- 必须包含：称呼 + 正文 + 结尾；",
      "- 语气正式、清晰，避免口语化表情；",
      "- 必须包含：你将如何推进 + 时间点/同步节奏 + 需要对方确认/提供的信息；",
      "- 可用简短分段，但不要 Markdown 标题。",
    ].join("\n");
  }
  return "用户没有选择回复输出形式，reply 必须返回 null。";
}

type BuildAnalyzePromptInput = {
  message: string;
  hasImage: boolean;
  identity: Identity;
  outputFormat?: OutputFormat;
  personName?: string;
  personProfile?: string;
};

export function buildAnalyzePrompt({
  message,
  hasImage,
  identity,
  outputFormat,
  personName,
  personProfile,
}: BuildAnalyzePromptInput) {
  const visionHint =
    hasImage && process.env.AI_ENABLE_VISION === "true"
      ? "本次输入包含截图：请先识别截图内文字、参与者身份（谁说的）、关键上下文；若截图文字不清晰，明确说明“截图内容不清晰，需要用户补充文字”。再结合用户补充文字分析。"
      : "本次输入为纯文字：请基于文字内容与上下文分析。";

  const personHint = personName
    ? [
        `本次指定具体人物：${personName}`,
        personProfile
          ? `该人物已有画像（供你参考，生成 reply 时要符合其偏好与雷区）：\n${personProfile}`
          : "该人物暂无画像：请从本次内容提取“可长期复用”的沟通特征，输出到 personProfileUpdate（不要记录隐私细节或一次性任务内容）。",
      ].join("\n")
    : "本次未指定具体人物：personProfileUpdate 必须返回 null。";

  return [
    "你是一个中文沟通意图分析助手。",
    "你要处理的输入可能来自：聊天记录、邮件正文、单方任务叙述、截图（微信/飞书/邮件截图）。",
    visionHint,
    "",
    "【总原则】",
    "1) 不要过度脑补：没有证据的推断必须写“可能/需要确认”。",
    "2) 输出必须可执行：不要泛泛总结，要给出能立刻行动的方案。",
    "3) 若信息不足：必须在 solutionOutline 里写“需要确认：...”并提出具体问题。",
    "4) 任何情况下都不要输出 Markdown、不要输出代码块、不要输出额外解释文字，只能输出 JSON。",
    "",
    `【对方身份】${identity}`,
    `【身份指导】\n${identityGuides[identity]}`,
    "",
    `【回复形式】${outputFormat ?? "不生成"}`,
    replyGuide(outputFormat),
    "",
    `【人物画像】\n${personHint}`,
    "",
    "【输出 JSON 契约（必须严格遵守）】",
    "必须只输出一个 JSON 对象，字段必须且只能为：realIntent, solutionOutline, risks, reply, personProfileUpdate。",
    "",
    "字段规则：",
    "- realIntent：字符串数组，1~3 条。每条<=35字。写“对方真实想要的结果/态度/动作”。若不确定，用“可能/需要确认”。",
    "- solutionOutline：字符串数组，0~6 条。每条以动词开头（确认/整理/输出/同步/推进/提醒/复盘等）。必须包含：你下一步要做什么 + 时间/节奏（如能推断）。缺信息则加：需要确认：问题1；问题2。",
    "- risks：字符串数组，0~5 条。至少覆盖 2 个维度：范围/时间/依赖/关系/承诺/验收口径/信息缺口。没明显风险可返回空数组。",
    "- reply：字符串或 null。只有当用户选择了微信或邮件时才生成；否则必须为 null。",
    "- personProfileUpdate：字符串或 null。只有当指定了 personName 时才生成；否则必须为 null。",
    "",
    "personProfileUpdate 写作要求（只有指定人物时）：",
    "- 用简短 Markdown（允许换行与小标题），输出“长期画像”，包含：语言风格/偏好、压力信号、边界/雷区、建议回复方式。",
    "- 不要记录隐私信息（手机号、地址、账号、金额等），不要记录一次性任务细节（例如某次具体文件名/金额/具体数据）。",
    "- 80~200 字左右，越精炼越好。",
    "",
    `【文字输入】${message?.trim() ? message.trim() : "无"}`,
  ].join("\n");
}
